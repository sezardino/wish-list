import { AuthProvider } from "@/context/Auth";
import { getNextAuthSession } from "@/libs/next-auth";
import {
  SupportedLanguages,
  getI18nLocale,
  i18nDefaultLanguage,
} from "@/libs/next-intl";
import "@/styles/main.css";
import { Theme } from "@radix-ui/themes";
import type { Metadata } from "next";
import { NextIntlClientProvider } from "next-intl";
import { Inter } from "next/font/google";
import { notFound } from "next/navigation";
import { PropsWithChildren } from "react";
import { ToastContainer } from "react-toastify";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

const localeHandler = async (locale: SupportedLanguages) => {
  try {
    return await getI18nLocale(locale);
  } catch (error) {
    notFound();
  }
};

const RootLayout = async ({ children }: PropsWithChildren) => {
  const session = await getNextAuthSession();
  const messages = await localeHandler("en");

  return (
    <html lang={i18nDefaultLanguage}>
      <body className={inter.className}>
        <AuthProvider session={session}>
          <NextIntlClientProvider
            locale={i18nDefaultLanguage}
            messages={messages}
          >
            <Theme className="min-h-screen">
              {children}
              <ToastContainer />
            </Theme>
          </NextIntlClientProvider>
        </AuthProvider>
      </body>
    </html>
  );
};

export default RootLayout;
